function slugifyPackageTitle(title) {
  return title
    .toLowerCase()
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

const CATEGORY_ENTRIES = Object.entries(CATEGORY_PACKAGES);

export const PACKAGES = Array.from(
  {
    length: Math.max(...CATEGORY_ENTRIES.map(([, items]) => items.length)),
  },
  (_, itemIndex) =>
    CATEGORY_ENTRIES.flatMap(([category, items]) => {
      const item = items[itemIndex];

      if (!item) {
        return [];
      }

      return [
        {
          ...item,
          slug: slugifyPackageTitle(item.title),
          category,
          image: CATEGORY_IMAGES[category][itemIndex % CATEGORY_IMAGES[category].length],
        },
      ];
    })
).flat();

export const CATEGORIES = ["All", ...Object.keys(CATEGORY_PACKAGES)];

const PACKAGES_BY_SLUG = new Map(PACKAGES.map((pkg) => [pkg.slug, pkg]));

export function getPackageBySlug(slug) {
  return PACKAGES_BY_SLUG.get(slug) || null;
}